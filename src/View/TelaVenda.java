    private void preencherLista() {
        ProdutoDAO pd = new ProdutoDAO();
        listaItens = new ArrayList<Produto>(pd.selecionarTodos());
        DefaultComboBoxModel cbmodel = new DefaultComboBoxModel();
        for (Produto p : listaItens) {
            cbmodel.addElement(p.getNome() + " - Quantidade: " + p.getQtda() + " - " + p.getPreco());
        }
        this.jComboItens.setModel(cbmodel);
    }

