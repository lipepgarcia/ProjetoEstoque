    // le os dados do formulario e retorna um objeto Produto
    public Produto lerForm(){
        Produto f = new Produto();
        f.setCod( Integer.valueOf("0" +eCod.getText()) );
        f.setNome(eNome.getText() );
        f.setQtda( Integer.valueOf(eQtd.getText()) );
        f.setEstoque( Integer.valueOf(eEst.getText()) );
        f.setPreco( Float.valueOf(ePrec.getText()) );
        
        return f;
    }
    
    // exibe um dado objeto Produto no formulario
    public void exibirNoForm(Produto f){
        eCod.setText( Integer.toString(f.getCod()) );
        eNome.setText( f.getNome() );
        eQtd.setText( Integer.toString(f.getQtda()) );
        eEst.setText( Integer.toString(f.getEstoque()) );
        ePrec.setText( Float.toString(f.getPreco()) );
      
    }
    
    //
    public void selecionarDaTab(){
        int linha = eTab.getSelectedRow();
        eCod.setText(eTab.getModel().getValueAt(linha, 0).toString());
        eNome.setText(eTab.getModel().getValueAt(linha, 1).toString());
        eQtd.setText(eTab.getModel().getValueAt(linha, 2).toString());
        eEst.setText(eTab.getModel().getValueAt(linha, 3).toString());
        ePrec.setText(eTab.getModel().getValueAt(linha, 4).toString());
    }

    // exibe uma dada lista de objetos Produto na tabela
    public void exibirNaTab(List<Produto> l){
        DefaultTableModel tabMod = (DefaultTableModel) eTab.getModel();
        tabMod.setRowCount(0);
        
        for(Produto f : l){
            String[] row = new String[5];  
            row[0] = (String.valueOf(f.getCod()));
            row[1] = f.getNome();
            row[2] = (String.valueOf(f.getQtda()));
            row[3] = (String.valueOf(f.getEstoque()));
            row[4] = (String.valueOf(f.getPreco()));
            
            tabMod.addRow(row);
        }
    }
    
    public void limparForm(){
        eCod.setText( "" );
        eNome.setText( "" );
        eQtd.setText( "" );
        eEst.setText( "" );
        ePrec.setText( "" );
        
    }

