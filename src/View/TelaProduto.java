    private void eDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eDeletarActionPerformed
        if(!"".equals(eCod.getText())){
            if(JOptionPane.showConfirmDialog(this, "Deseja Deletar esse Produto?") == 0){
                if(control.deletar()){
                    exibirNaTab(control.selecionarTodos());
                    limparForm();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Falha ao Deletar Produto");
                }
            }
        }
        else JOptionPane.showMessageDialog(this, "Selecione um Produto na Tabela");
    }//GEN-LAST:event_eDeletarActionPerformed

