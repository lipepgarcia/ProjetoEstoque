    private void eIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eIncluirActionPerformed
        if(!"".equals(eNome.getText())){
            if(control.inserir()){
                // exibir a lista na tabela
                exibirNaTab(control.selecionarTodos());
                limparForm();
                JOptionPane.showMessageDialog(this, "Produto Incluido");
            }
            else{
                JOptionPane.showMessageDialog(this, "Falha Inserção");
            }
        }
        else JOptionPane.showMessageDialog(this, "Descreva o Nome do Produto");
    }//GEN-LAST:event_eIncluirActionPerformed

    private void eAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eAtualizarActionPerformed
        if(!"".equals(eCod.getText()) ){
            if(control.atualizar()){
                exibirNaTab(control.selecionarTodos());
                limparForm();
                JOptionPane.showMessageDialog(this, "Produto Atualizado");
            }
            else{
                JOptionPane.showMessageDialog(this, "Falha Atualização");
            }
            
        }
        else JOptionPane.showMessageDialog(this, "Selecione um Produto na Tabela");
    }//GEN-LAST:event_eAtualizarActionPerformed

